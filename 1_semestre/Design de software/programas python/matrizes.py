import numpy as np

print("matriz de zeros")
matriz_de_zeros = np.zeros([3,3])
print(matriz_de_zeros)
print()

print("matriz de um")
matriz_de_ones = np.ones([3,3])
print(matriz_de_ones)
print()

print("matriz comum")
matriz = np.array ([1,2,3])
print(matriz)
print()

print("matriz de identidade")
matriz_de_identidade = np.identity(3)
print(matriz_de_identidade)
print()

print(2*matriz_de_identidade)